#include "sprite_renderer.h"

sprite_renderer::sprite_renderer(std::map<std::string, Shader> &shaders)
{
    this->shaders = &shaders;
    this->initRenderData();
}

sprite_renderer::~sprite_renderer()
{
    glDeleteVertexArrays(1, &this->quadVAO);
    glDeleteVertexArrays(1, &this->lightVAO);

}

/* Renderiza el quad definido con la textura dada */
void
sprite_renderer::drawSprite(/*Texture2D& texture, glm::vec2 position, glm::vec2 size, float rotate, glm::vec3 color*/)
{
    (*shaders)["colors"].Use();
    glBindVertexArray(this->quadVAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    (*shaders)["light_cube"].Use();
    glDrawArrays(GL_TRIANGLES, 0, 36);
}

